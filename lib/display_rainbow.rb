def display_rainbow(input)
  colors = ['red', 'orange', 'yellow', 'green', 'blue', 'indigo', 'violet']
  if input == colors
    puts "R: #{colors[0]}, O: #{colors[1]}, Y: #{colors[2]}, G: #{colors[3]}, B: #{colors[4]}, I: indigo, V: #{colors[5]}\n"
  end
end 